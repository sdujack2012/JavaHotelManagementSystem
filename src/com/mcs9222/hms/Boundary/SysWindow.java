    public void setPermissionByRole(String name, String role) {
        userLabel.setText(role + ": " + name);
        // by default, all functional tabs are unavailable
        mainTabbedPane.setEnabledAt(0, false);
        mainTabbedPane.setEnabledAt(1, false);
        mainTabbedPane.setEnabledAt(2, false);
        mainTabbedPane.setEnabledAt(3, false);

        reservationTabbedPane.setVisible(false);
        serviceTabbedPane.setVisible(false);
        profileTabbedPane.setVisible(false);
        reportTable.setVisible(false);
        if (role.equals("Manager")) {
            mainTabbedPane.setSelectedIndex(3);
            mainTabbedPane.setEnabledAt(3, true);
            DefaultListModel listModel = new DefaultListModel();
            listModel.addElement("Checkin Report");
            listModel.addElement("Checkout Report");
            listModel.addElement("Revenue Summary");
            listModel.addElement("Booking Activity");
            reportTypesList.setModel(listModel);

        } else if (role.equals("Receptionist")) {
            mainTabbedPane.setEnabledAt(0, true);
            mainTabbedPane.setEnabledAt(1, true);
            mainTabbedPane.setEnabledAt(2, true);
            mainTabbedPane.setEnabledAt(3, true);
            reservationTabbedPane.setVisible(true);
            serviceTabbedPane.setVisible(true);
            profileTabbedPane.setVisible(true);
            DefaultListModel listModel = new DefaultListModel();
            listModel.addElement("Expected Arrivals");
            reportTypesList.setModel(listModel);
            ppc.setTableModelByDefault();
            pac.setTableModelByDefault();

        } else if (role.equals("Service Staff")) {
            mainTabbedPane.setEnabledAt(2, true);
            mainTabbedPane.setSelectedIndex(2);
            serviceTabbedPane.setVisible(true);
            serviceTabbedPane.setEnabledAt(0, false);
            serviceTabbedPane.setEnabledAt(1, false);
            serviceTabbedPane.setSelectedIndex(2);
        }
    }

