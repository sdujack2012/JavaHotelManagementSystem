    private void reservationResultsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_reservationResultsTableMouseClicked
        int selectedRow = reservationResultsTable.getSelectedRow();
        String roomNo = rac.getRoomNo(selectedRow);

        Date startDate = null;
        Date endDate = null;
        try {
            startDate = new SimpleDateFormat("dd/MM/yy").parse(this.reservationStartDateTextField.getText());
            endDate = new SimpleDateFormat("dd/MM/yy").parse(this.reservationEndDateTextField.getText());
        } catch (ParseException ex) {
            Logger.getLogger(SysWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
        CustomerInfoBookingWindow cibw = new CustomerInfoBookingWindow(roomNo, startDate, endDate, rac, selectedRow);
        cibw.setVisible(true);
    }//GEN-LAST:event_reservationResultsTableMouseClicked

